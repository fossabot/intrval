"%[]%" <- function(x, interval)
    intrval(x, interval, "[]")
"%[)%" <- function(x, interval)
    intrval(x, interval, "[)")
"%(]%" <- function(x, interval)
    intrval(x, interval, "(]")
"%()%" <- function(x, interval)
    intrval(x, interval, "()")

"%][%" <- function(x, interval)
    !intrval(x, interval, "()")
"%](%" <- function(x, interval)
    !intrval(x, interval, "(]")
"%)[%" <- function(x, interval)
    !intrval(x, interval, "[)")
"%)(%" <- function(x, interval)
    !intrval(x, interval, "[]")
